namespace ModernWinForms.Theming;

/// <summary>
/// Available theme design systems.
/// </summary>
public enum Theme
{
    /// <summary>
    /// Base/Default theme - simple, clean design.
    /// </summary>
    Base,

    /// <summary>
    /// GitHub-inspired theme - clean, professional.
    /// </summary>
